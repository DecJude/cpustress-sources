int 
setup_md_data( md_data_t *  data )
{
    int i;
    
    /* system coordinates */
    data->natoms   = init_natoms;
    data->x        = malloc(sizeof(float)*init_natoms*3);
    data->shiftvec = malloc(sizeof(float)*27*3);
    data->charge   = malloc(sizeof(float)*init_natoms);
    data->type     = malloc(sizeof(int)*init_natoms);
    data->ntype    = 2;
    data->nbparam  = malloc(sizeof(float)*2*data->ntype*data->ntype);
    
    for(i=0;i<init_natoms*3;i++)
    {
        data->x[i] = init_x[i];
    }
    for(i=0;i<init_natoms/3;i++)
    {
        data->charge[3*i]   = -0.82;
        data->type[3*i]     = 0;
        data->charge[3*i+1] = 0.41;
        data->charge[3*i+2] = 0.41;
        data->type[3*i+1]   = 1;
        data->type[3*i+2]   = 1;
    }
    
    for(i=0;i<2*data->ntype*data->ntype;i++)
    {
        data->nbparam[i] = 0;
    }
    data->nbparam[0] = 0.26171E-02;
    data->nbparam[1] = 0.26331E-05;
    
    for(i=0;i<27*3;i++)
    {
        data->shiftvec[i] = 0;
    }

    /* Neighborlist */
    data->nlist.nri     = init_nlist_nri;
    data->nlist.iinr    = malloc(sizeof(int)*init_nlist_nri);
    data->nlist.gid     = malloc(sizeof(int)*init_nlist_nri);
    data->nlist.shift   = malloc(sizeof(int)*init_nlist_nri);
    data->nlist.jindex  = malloc(sizeof(int)*(init_nlist_nri+1));
    data->nlist.jjnr    = malloc(sizeof(int)*INIT_NLIST_NJJNR);
    
    for(i=0;i<init_nlist_nri;i++)
    {
        data->nlist.iinr[i]   = init_nlist_iinr[i];
        data->nlist.gid[i]    = init_nlist_gid[i];
        data->nlist.shift[i]  = init_nlist_shift[i];
        data->nlist.jindex[i] = init_nlist_jindex[i]; 
    }
    data->nlist.jindex[init_nlist_nri] = init_nlist_jindex[init_nlist_nri];
    
    for(i=0;i<INIT_NLIST_NJJNR;i++)
    {
        data->nlist.jjnr[i] = init_nlist_jjnr[i];
    }
    
    return 0;
}


void
release_md_data(md_data_t *  data)
{
    free(data->x);
    free(data->shiftvec);
    free(data->charge);
    free(data->type); 
    free(data->nbparam);

    free(data->nlist.iinr);
    free(data->nlist.gid);
    free(data->nlist.shift);
    free(data->nlist.jindex);
    free(data->nlist.jjnr);
}

